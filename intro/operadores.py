# Operadores aritiméticos

# soma

numero1 = 10
numero2 = 25

resultado = numero1 + numero2

print(resultado)

#subtração

resultado = numero1 - numero2
print(resultado)

# multiplicação

resultado = numero1*numero2
print(resultado)

# divisão

resultado = numero1/numero2
print(resultado)


# modulo

resultado = numero1%numero2
print(resultado)

#exponenciação
numero1 = 4
numero2 = 2

resultado = numero1**numero2
print(resultado)

